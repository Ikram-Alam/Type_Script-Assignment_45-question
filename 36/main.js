// Define the function make_shirt
function make_shirt(size, message) {
    console.log("The shirt size is ".concat(size, " and the message on it is: \"").concat(message, "\""));
}
// Call the function make_shirt with example arguments
make_shirt('L', 'Hello World!');
make_shirt('M', 'TypeScript Rocks!');
make_shirt('S', 'Coding is fun!');
