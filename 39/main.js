// Define the function city_country
function city_country(city, country) {
    return "".concat(city, ", ").concat(country);
}
// Call the function city_country with different city-country pairs
var city1 = city_country('Lahore', 'Pakistan');
var city2 = city_country('New York', 'USA');
var city3 = city_country('Tokyo', 'Japan');
// Print the values returned by the function
console.log(city1);
console.log(city2);
console.log(city3);
